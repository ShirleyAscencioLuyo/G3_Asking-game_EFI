CREATE TABLE Usuario
(
	idusu		int AUTO_INCREMENT PRIMARY KEY NOT NULL ,
	nomusu		varchar(80) not NULL,		-- nombre
	apeusu		varchar(80) not null,	-- Apellido
	celusu		char(9) not null,		-- celular
